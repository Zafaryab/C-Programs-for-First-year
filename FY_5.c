#include<stdio.h>

int main()
{
    int a = 5, b = 10;

    printf("\n\nRelational operators (a=%d,b=%d): \n", a, b);
    printf("a == b : %d\n", (a==b));
    printf("a != b : %d\n", (a!=b));
    printf("a >= b : %d\n", (a>=b));
    printf("a  > b : %d\n", (a>b));
    printf("a <= b : %d\n", (a<=b));
    printf("a  < b : %d\n", (a<b));
    
}    